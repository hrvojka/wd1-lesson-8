mood = input("Enter your mood: ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Smile, happiness is overrated :)")
elif mood == "excited":
    print("It is great to see you excited!")
elif mood == "relaxed":
    print("It is great to see you relaxed!")
else:
    print("I don't recognize this mood.")